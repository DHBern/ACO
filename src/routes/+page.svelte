	<h2>Die Akten des Konzils von Ephesus 431</h2>
	<p>
		Das Konzil von Ephesus wurde 431, von Kaiser Theodosius II einberufen. Die Geschehnisse rund um
		das Konzil von Ephesus sind uns in zahlreichen Akten überliefert. Die Akten wurden in den Jahren
		1923–29 von Eduard Schwartz in einer epochalen, bis heute maßgeblichen Ausgabe ediert. Er beließ
		die altsprachlichen Texte dabei in der Anordnung, die durch die einzelnen Sammlungen vorgegeben
		war. Ziel unseres Projektes ist es, die Akten des Konzils erstmalig vollständig in eine moderne
		Sprache zu übersetzen, chronologisch zu ordnen und zu kommentieren. Das Projekt wird von der DFG
		gefördert und ist als Langfristvorhaben auf einen Zeitraum von zehn Jahren angesetzt
		(2016–2026).
	</p>
